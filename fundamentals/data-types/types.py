# Numeric
print("\nNumeric")
print(type(10))
print(type(10.1))
print(type(10+10j))
print(type(10 > 9))

# Type Conversion
print("\nType Conversion")
print(int(10.10))
print(float(10))
print(complex(1, 2))
print(bool(10))

# Sequence
print("\nSequence")
print(type([1, 2]))
print(type((1, 2)))
print(type({1, 2}))
print(type("1,2"))
print(type(range(1, 2)))

# Dict
print("\nDictionary")
print(type({1: "One", 2: "Two"}))
